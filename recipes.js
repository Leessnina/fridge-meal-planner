// 用户通过网页添加的菜谱会存储在 localStorage 中，
// 运行时自动合并，无需在此处理。

function loadCustomRecipes() {
  try {
    const saved = localStorage.getItem('fridge_custom_recipes');
    return saved ? JSON.parse(saved) : [];
  } catch(e) { return []; }
}

function getAllRecipes() {
  return [...RECIPES, ...loadCustomRecipes()];
}
